package com.vasu.java;

public class NestedIfStatement {

	public static void main(String[] args) 
	{
		int n=2;
		if (n==1)
		{
			System.out.println("Monday");
		}else if(n==2)
		{
			System.out.println("Tuesday");
		}else if(n==3)
		{
			System.out.println("Wednessday");
		}else if(n==4)
		{
			System.out.println("Thursday");
		}

	}

}
